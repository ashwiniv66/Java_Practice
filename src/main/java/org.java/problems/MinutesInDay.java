package org.example.problems;

public class MinutesInDay {
    public static void main(String[] args) {
       int  minutesPerHour  = 60 ;
       int  hoursPerDay  = 24 ;

       int minutesInDay = minutesPerHour * hoursPerDay;

        System.out.println("Minutes in a day is "+minutesInDay);
    }
}
